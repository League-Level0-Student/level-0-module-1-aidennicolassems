import javax.swing.JOptionPane;

public class Remarkable {
public static void main(String[] args) {

		// 1. Save something remarkable about each person in a variable.
	
		
	 String Joe = "He is weird";
	 
	 String Sue = "She is ugly";
	 
	 String Dayton = "He is also weird";
	 
	 
	 
	 
	 
	 
	 
	 // 2. Ask the user to enter a name. Store their answer in a variable.
	 String input = JOptionPane.showInputDialog("What is your name?");
		// 3. In a pop-up, tell the user what is remarkable about that person. 
	
	 if (input.contains("Joe")) {
	 
	 
	 
	
	 JOptionPane.showMessageDialog(null,  Joe    );
	 
	 }
	 
	 
	 

	 if (input.contains("Sue")) {
	 
	 
	 
	
	 JOptionPane.showMessageDialog(null,  Sue    );
	 
	 }
	 
	 
	 
	 

	 if (input.contains("Dayton")) {
	 
	 
	 
	
	 JOptionPane.showMessageDialog(null, Dayton    );
	 
	 }
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 
	}
}

